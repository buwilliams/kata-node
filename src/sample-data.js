'use strict';

function oneToNine() {
    var data = [];                                   
    data[0] = '    _  _     _  _  _  _  _ ';         
    data[1] = '  | _| _||_||_ |_   ||_||_|';         
    data[2] = '  ||_  _|  | _||_|  ||_| _|';         
    data[3] = '                           ';
    return data;
}

exports.oneToNine = oneToNine();
